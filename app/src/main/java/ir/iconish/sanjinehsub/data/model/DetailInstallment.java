package ir.iconish.sanjinehsub.data.model;

public class DetailInstallment {
private int typesAmount;
private int reportsHistoricalCalendarMonth;
private int reportsHistoricalCalendarYear;

    public int getTypesAmount() {
        return typesAmount;
    }

    public void setTypesAmount(int typesAmount) {
        this.typesAmount = typesAmount;
    }

    public int getReportsHistoricalCalendarMonth() {
        return reportsHistoricalCalendarMonth;
    }

    public void setReportsHistoricalCalendarMonth(int reportsHistoricalCalendarMonth) {
        this.reportsHistoricalCalendarMonth = reportsHistoricalCalendarMonth;
    }

    public int getReportsHistoricalCalendarYear() {
        return reportsHistoricalCalendarYear;
    }

    public void setReportsHistoricalCalendarYear(int reportsHistoricalCalendarYear) {
        this.reportsHistoricalCalendarYear = reportsHistoricalCalendarYear;
    }
}
