package ir.iconish.sanjinehsub.data.model;

public class Person {

    private String name;
    private String family;
    private String ntCode;
    private String birthDate;
    private String birthCity;
    private String fatherName;
    private String address;
    private String gender;
    private String borrowerClass;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFamily() {
        return family;
    }

    public void setFamily(String family) {
        this.family = family;
    }

    public String getNtCode() {
        return ntCode;
    }

    public void setNtCode(String ntCode) {
        this.ntCode = ntCode;
    }

    public String getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(String birthDate) {
        this.birthDate = birthDate;
    }

    public String getBirthCity() {
        return birthCity;
    }

    public void setBirthCity(String birthCity) {
        this.birthCity = birthCity;
    }

    public String getFatherName() {
        return fatherName;
    }

    public void setFatherName(String fatherName) {
        this.fatherName = fatherName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getBorrowerClass() {
        return borrowerClass;
    }

    public void setBorrowerClass(String borrowerClass) {
        this.borrowerClass = borrowerClass;
    }
}
